function [dx, st] = SLS_fun(t,x)
%% 功能：dx = SLS_fun(t,x) 切换系统状态方程
% 输入：时间t,状态x
% 输出：导数dx,切换信号st
A1=[0 10;0 0];
A2=[1.5 2;-2 -0.5];
%% 当x2-0.5x1>=0且x2+0.25x1>=0时或x2-0.5x1<=0且x2+0.25x1<=0时切换到子系统1，其余情况切换到子系统2
if((x(2)-0.5*x(1)>=0&&x(2)+0.25*x(1)>=0)||(x(2)-0.5*x(1)<=0&&x(2)+0.25*x(1)<=0))
   st=1;
   A=A1;
else
   st=2;
   A=A2;
end
dx=A*x;